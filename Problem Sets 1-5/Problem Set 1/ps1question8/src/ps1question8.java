
public class ps1question8 
{
	public static void main(String[] args)
	{
		int num = 0;
		int counter = 100;
		
		while (counter > num )
		{	
			if (counter == 1)
			{
				System.out.println(counter);
			}
			else
			{
				System.out.print(counter + ", ");
			}
			
			counter --;
		}
	}
}
